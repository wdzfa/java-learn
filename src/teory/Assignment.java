package teory;

public class Assignment {

    public static void main(String[] args){

        // Operator penjumlahan
        int a = 1;
        a += 10; // sama halnya dengan a = a + 10
        System.out.println("nilai a = " + a);

        // Operator pengurangan
        int b = 100;
        b -= 10; // sama halnya dengan b = b - 10
        System.out.println("nilai b = " + b);

        // Operator perkalian
        int c = 10;
        c *= 10; // sama halnya dengan b = b * 10
        System.out.println("nilai c = " + c);

        // Operator pembagian
        int d = 100;
        d /= 20; // sama halnya dengan b = 100 / 10
        System.out.println("nilai d = " + d);

        // Operator modulus
        int e = 10;
        e %= 7; // sama halnya dengan b = 10 % 7
        System.out.println("nilai e = " + e);
    }
}
